def delete():
    d.reset()
    print("Doodle Reset")
    d.speed(50)
    d.pensize(5)
def move_pen_to_click(x,y):
    d.penup()
    d.goto(x,y)
    d.pendown()
def save_art():
    canvas = turtle.getcanvas()
    canvas.postscript(file="drawing.eps")
    print("Doodle saved")
def begin_fill():
    d.begin_fill()
    print("Filling")
def end_fill():
    d.end_fill()
    print("Filled")
screen.onkey(change_color_red,"1")  
screen.onkey(change_color_blue,"2")  
screen.onkey(change_color_green,"3")
screen.onkey(change_color_yellow, "4") 
screen.onkey(change_color_black, "5")  
screen.onkey(change_color_pink, "6")
screen.onkey(change_color_white, "space")
screen.onkey(delete,"x")
screen.onkey(thickness,"t")
screen.onkey(save_art,"s")
screen.onkey(begin_fill,"b")
screen.onkey(end_fill,"e")
turtle.Screen().onscreenclick(move_pen_to_click)
screen.listen()
d.ondrag(drag_handler)
turtle.done()   
