def drag_handler(x, y):
    d.ondrag(None) 
    d.setheading(d.towards(x, y)) 
    d.goto(x, y) 
    d.ondrag(drag_handler)       
def change_color_red():
    d.color("red")
    print("Pen red")
def change_color_blue():
    d.color("blue")
    print("Pen blue")
def change_color_green():
    d.color("green")
    print("Pen green")
def change_color_yellow():
    d.color("yellow")
    print("Pen yellow")
def change_color_black():
    d.color("black")
    print("Pen black")
def change_color_pink():
    d.color("pink")
    print("Pen pink")
def change_color_white():
        d.color("red")
        time.sleep(1.1)
        d.color("white")
        print("Eraser On")
